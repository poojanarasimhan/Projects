        if(rowSelected<0){
            JOptionPane.showMessageDialog(this, "Select a row");
            return;
        }
        WorkRequest1 wr1 =(WorkRequest1) jTable1.getValueAt(rowSelected, 0);
        wr1.setStatus("Approved");
        UserAccount ua =wr1.getSender();
        ua.getEmployee().setAddressLine1(wr1.getAddLine1());
        ua.getEmployee().setAddressLine2(wr1.getAddLine2());
        ua.getEmployee().setZipCode(wr1.getZipCode());
        populateTable();
        
        
