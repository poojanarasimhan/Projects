    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                            dtm.setRowCount(0);
                             for(Organization o : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if(o.getName().equals("Manager Organization")) {
             for(WorkRequest1 wr1: o.getWorkQueue1().getWorkRequestList1() )  {
                  Object row[] = new Object[6];
            row[0] = wr1;
            row[1] = wr1.getAddLine2();
            
            row[2] = wr1.getZipCode();
            row[3] = wr1.getSender();
            row[4] = wr1.getStatus();
            dtm.addRow(row);
             }
            }
        }
    }

